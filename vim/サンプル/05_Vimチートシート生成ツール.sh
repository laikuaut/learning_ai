#!/bin/bash
# ============================================================
# Vimチートシート生成ツール
# ============================================================
# 学べる内容:
#   - Vimの主要コマンド一覧（移動・編集・検索・ファイル操作等）
#   - コマンドのカテゴリ別整理
#   - 見やすいテキスト形式のチートシート作成
#
# 実行方法:
#   chmod +x 05_Vimチートシート生成ツール.sh
#   ./05_Vimチートシート生成ツール.sh
# ============================================================

set -e

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
RESET='\033[0m'

TMPDIR=$(mktemp -d)
trap 'rm -rf "$TMPDIR"' EXIT

section() { echo -e "\n${GREEN}========== $1 ==========${RESET}\n"; }
info()    { echo -e "${CYAN}[INFO]${RESET} $1"; }
run_cmd() { echo -e "${YELLOW}\$ $1${RESET}"; eval "$1"; }
pause()   { echo -e "\n${CYAN}[Enter を押して次へ進む]${RESET}"; read -r; }

CHEATSHEET="$TMPDIR/vim_cheatsheet.txt"

section "Vimチートシート生成ツール"
info "カテゴリ別に整理されたVimチートシートを生成します。"
pause

# ----------------------------------------------------------
section "1. チートシートを生成中..."

cat > "$CHEATSHEET" << 'EOF'
╔══════════════════════════════════════════════════════════════╗
║               Vim チートシート（日本語版）                    ║
╚══════════════════════════════════════════════════════════════╝

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【モード切替】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  i       カーソルの前に挿入
  a       カーソルの後ろに挿入
  I       行頭に挿入
  A       行末に挿入
  o       次の行に新規行を作成して挿入
  O       前の行に新規行を作成して挿入
  v       ビジュアルモード（文字選択）
  V       ビジュアルモード（行選択）
  Ctrl+v  ビジュアルモード（矩形選択）
  Esc     ノーマルモードに戻る

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【カーソル移動】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  h       左に移動
  j       下に移動
  k       上に移動
  l       右に移動
  w       次の単語の先頭へ
  b       前の単語の先頭へ
  e       単語の末尾へ
  0       行頭へ
  ^       行頭（空白を除く）へ
  $       行末へ
  gg      ファイルの先頭へ
  G       ファイルの末尾へ
  数字G   指定行へジャンプ（例: 10G）
  Ctrl+f  1画面下にスクロール
  Ctrl+b  1画面上にスクロール
  Ctrl+d  半画面下にスクロール
  Ctrl+u  半画面上にスクロール
  %       対応する括弧へジャンプ

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【編集】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  x       カーソル位置の文字を削除
  dd      行を削除（カット）
  dw      単語を削除
  d$      カーソルから行末まで削除
  D       d$ と同じ
  yy      行をコピー（ヤンク）
  yw      単語をコピー
  p       カーソルの後に貼り付け
  P       カーソルの前に貼り付け
  u       元に戻す（Undo）
  Ctrl+r  やり直し（Redo）
  .       直前の操作を繰り返す
  r       1文字置き換え
  R       上書きモード
  cc      行を変更（削除して挿入モード）
  cw      単語を変更
  c$      カーソルから行末まで変更
  >>      インデントを追加
  <<      インデントを削除
  J       次の行と結合

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【検索・置換】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  /pattern         前方検索
  ?pattern         後方検索
  n                次の検索結果へ
  N                前の検索結果へ
  *                カーソル位置の単語を前方検索
  #                カーソル位置の単語を後方検索
  :s/old/new/      現在行の最初の一致を置換
  :s/old/new/g     現在行の全一致を置換
  :%s/old/new/g    ファイル全体で置換
  :%s/old/new/gc   確認付きで全体置換
  :noh             検索ハイライトを消す

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【ファイル操作】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  :w               保存
  :w filename      別名で保存
  :q               終了（変更なし時）
  :q!              変更を破棄して終了
  :wq              保存して終了
  :x               変更があれば保存して終了
  ZZ               :x と同じ
  :e filename      ファイルを開く
  :r filename      ファイルの内容を挿入
  :r !command      コマンドの出力を挿入

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【ウィンドウ・タブ】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  :split           水平分割
  :vsplit          垂直分割
  Ctrl+w h/j/k/l   ウィンドウ間を移動
  Ctrl+w +/-       ウィンドウサイズ変更
  :tabnew          新しいタブ
  gt / gT          次/前のタブへ移動
  :close           ウィンドウを閉じる

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  【便利なコマンド】
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  :set number      行番号を表示
  :set nonumber    行番号を非表示
  :!command        外部コマンドを実行
  :sort            行をソート
  :sort u          重複を除いてソート
  gg=G             ファイル全体を自動インデント
  :marks           マーク一覧を表示
  ma               マーク a を設定
  'a               マーク a にジャンプ
  q{a-z}           マクロの記録開始
  q                マクロの記録終了
  @{a-z}           マクロの実行

EOF

info "チートシートの生成が完了しました。"
pause

# ----------------------------------------------------------
section "2. 生成されたチートシートのプレビュー"
run_cmd "cat '$CHEATSHEET'"
pause

# ----------------------------------------------------------
section "3. チートシートの保存"
echo "チートシートをどこに保存しますか？"
echo "  1) 現在のディレクトリに vim_cheatsheet.txt として保存"
echo "  2) 表示のみ（保存しない）"
echo ""
read -rp "選択 [1-2] (デフォルト: 2): " save_choice
save_choice=${save_choice:-2}

if [[ "$save_choice" == "1" ]]; then
    cp "$CHEATSHEET" ./vim_cheatsheet.txt
    info "vim_cheatsheet.txt として保存しました。"
    info "印刷する場合: lpr vim_cheatsheet.txt"
else
    info "保存をスキップしました。"
fi

echo ""
info "チートシート生成ツール完了！一時ファイルは自動的に削除されます。"
